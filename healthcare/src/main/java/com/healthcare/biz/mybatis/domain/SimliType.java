package com.healthcare.biz.mybatis.domain;


public class SimliType {

	private String simliId;
	private String simliNm;
	private String intro;
	private String outro;
	private String useYN;
	
	public String getSimliId() {
		return simliId;
	}
	public void setSimliId(String simliId) {
		this.simliId = simliId;
	}
	public String getSimliNm() {
		return simliNm;
	}
	public void setSimliNm(String simliNm) {
		this.simliNm = simliNm;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	public String getOutro() {
		return outro;
	}
	public void setOutro(String outro) {
		this.outro = outro;
	}
	public String getUseYN() {
		return useYN;
	}
	public void setUseYN(String useYN) {
		this.useYN = useYN;
	}
	
	
	
	
	
}
